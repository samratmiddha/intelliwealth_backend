class Command(BaseCommand):
    help = 'Bulk create or update Asset entries for stock symbols and names'

    def handle(self, *args, **kwargs):
        created = 0
        updated = 0
        for stock in stockList:
            obj, is_created = Asset.objects.update_or_create(
                symbol=stock["symbol"],
                defaults={
                    "name": stock["name"],
                    "asset_type": "stock",
                    "exchange": "NYSE"
                }
            )
            if is_created:
                created += 1
            else:
                updated += 1
        self.stdout.write(self.style.SUCCESS(f'Successfully created {created} and updated {updated} stock assets.'))